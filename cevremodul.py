

def dikdortgenÇ(uzunkenar,kisakenar):
    """
    (Dikdörtgenin Çevresini hesaplar)
    (Girilen uzun ve kisa kenarı toplar 2 ile çarpar)
    """
    return (uzunkenar + kisakenar) * 2

def kareÇ(kenar1):
    """
    (Karenin Çevresini hesaplar)
    (Girilen kenarı 4 ile çarpar)
    """
    return (kenar1) * 4

def ucgenÇ(kenar1,kenar2,kenar3):
    """
    (üçgenin Çevresini Hesaplar)
    (Girilen 3 kenarı toplar)
    """
    return kenar1 + kenar2 + kenar3